// Array.prototype.includes()


// returns the boolean if element present in array or not 
// if element present in array returns true
// if element not present in array returns false

const arr = ['red', 'blue', 'black', 'white', 'green']
const isPresent = arr.includes('red')
console.log(isPresent)
const isNotPresent = arr.includes('python')
console.log(isNotPresent)